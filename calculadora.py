# Entrada de Dados

print('===== Calculadora =====')

n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))

soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2

# Saída de Dados

print(f'{n1} + {n2} = {soma}')
print(f'{n1} - {n2} = {sub}')
print(f'{n1} * {n2} = {mult}')
print(f'{n1} / {n2} = {div}')